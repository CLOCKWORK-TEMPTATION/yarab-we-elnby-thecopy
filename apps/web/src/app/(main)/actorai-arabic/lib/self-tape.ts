interface ExportFileNameInput {
  actorName: string;
  projectName: string;
  roleName: string;
  takeName: string;
  extension: string;
}

interface TakeInsightsInput {
  durationSeconds: number;
  scriptText: string;
  teleprompterUsed: boolean;
  hadRetake: boolean;
}

interface PersistedTakeNote {
  id: string;
  timestamp: number;
  type: "emotion" | "delivery" | "timing" | "movement" | "improvement";
  content: string;
  severity: "positive" | "neutral" | "needs_work";
  autoGenerated: boolean;
}

export interface PersistedSelfTapeTake {
  id: string;
  name: string;
  duration: number;
  recordedAt: string;
  notes: PersistedTakeNote[];
  score?: number;
  status: "recording" | "completed" | "exported";
  mimeType?: string;
  source: "sample" | "captured";
}

export interface TakeInsightNote {
  type: "timing" | "delivery" | "improvement";
  severity: "positive" | "neutral" | "needs_work";
  content: string;
}

export interface TakeInsights {
  score: number;
  notes: TakeInsightNote[];
}

