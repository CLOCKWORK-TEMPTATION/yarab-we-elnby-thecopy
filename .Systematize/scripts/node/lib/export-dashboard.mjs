// تصدير HTML dashboard — مكافئ export-dashboard.ps1
import { existsSync, readFileSync, writeFileSync } from 'fs';
import { join } from 'path';
import { getFeaturePathsEnv, getFeatureDir, getFeatureProgress, parseArgs, ensureDir } from './common.mjs';
import { isOptionalCapabilityEnabled } from './configuration.mjs';
import { evaluateFeatureHealth, getTaskCompletionStats } from './health.mjs';

export default async function main(argv) {
  const opts = parseArgs(argv);
  if (opts.help) { console.log('Usage: syskit export-dashboard [--branch <name>] [--output <path>] [--json]'); return; }

  const env = getFeaturePathsEnv();
  const branch = opts.branch || env.CURRENT_BRANCH;
  const featureDir = opts.branch ? getFeatureDir(env.REPO_ROOT, opts.branch) : env.FEATURE_DIR;

  if (!isOptionalCapabilityEnabled('export', env.REPO_ROOT)) {
    const result = {
      branch,
      outputPath: null,
      disabled: true,
      capability: 'export',
      message: 'Export capability disabled in configuration'
    };

    if (opts.json) {
      console.log(JSON.stringify(result));
    } else {
      console.log('Export capability disabled in configuration');
    }
    return;
  }

  if (!existsSync(featureDir)) { console.error(`❌ Feature not found: ${featureDir}`); process.exit(1); }

  const read = (f) => existsSync(f) ? readFileSync(f, 'utf8') : '';
  const sys = read(join(featureDir, 'sys.md'));
  const tasks = read(join(featureDir, 'tasks.md'));
  const research = read(join(featureDir, 'research.md'));
  const plan = read(join(featureDir, 'plan.md'));

  const frCount = (sys.match(/FR-\d{3}/g) || []).length;
  const nfrCount = (sys.match(/NFR-\d{3}/g) || []).length;
  const acCount = (sys.match(/AC-\d{3}/g) || []).length;
  const rkCount = (sys.match(/RK-\d{3}/g) || []).length;
  const { totalTasks: taskCount, completedTasks, completionPercent } = getTaskCompletionStats(tasks);
  const progress = getFeatureProgress(featureDir);
  const health = evaluateFeatureHealth(featureDir);
  const healthBadgeColor = health.score >= 70 ? '#10b981' : health.score >= 50 ? '#f59e0b' : '#ef4444';

  const html = `<!DOCTYPE html>
<html lang="en"><head><meta charset="UTF-8"><title>Dashboard: ${branch}</title>
<style>*{margin:0;padding:0;box-sizing:border-box}body{font-family:system-ui,sans-serif;background:#0f172a;color:#e2e8f0;padding:24px}.header{text-align:center;margin-bottom:32px}.header h1{font-size:1.8rem;color:#f8fafc}.grid{display:grid;grid-template-columns:repeat(auto-fit,minmax(250px,1fr));gap:16px}.card{background:#1e293b;border-radius:12px;padding:20px;border:1px solid #334155}.card h3{font-size:.875rem;color:#94a3b8;text-transform:uppercase}.card .value{font-size:2rem;font-weight:700;color:#f8fafc}.card .sub{font-size:.875rem;color:#64748b}.bar{width:100%;height:8px;background:#334155;border-radius:4px;margin-top:8px;overflow:hidden}.fill{height:100%;border-radius:4px;background:#10b981}.health-badge{background:${healthBadgeColor};border-radius:8px;padding:12px;text-align:center;color:#fff;font-weight:700}.health-badge .score{font-size:2rem}.health-badge .label{font-size:.75rem;margin-top:4px}.footer{text-align:center;margin-top:32px;color:#475569;font-size:.75rem}</style></head>
<body><div class="header"><h1>${branch}</h1></div>
<div class="grid">
<div class="card health-badge"><div class="label">Health</div><div class="score">${health.score}</div><div class="sub">/${health.maxScore} • ${health.status}</div></div>
<div class="card"><h3>Requirements</h3><div class="value">${frCount} FR</div><div class="sub">${nfrCount} NFR | ${acCount} AC</div></div>
<div class="card"><h3>Risks</h3><div class="value">${rkCount}</div></div>
<div class="card"><h3>Tasks</h3><div class="value">${taskCount}</div><div class="sub">${completedTasks} completed</div><div class="bar"><div class="fill" style="width:${completionPercent}%"></div></div></div>
<div class="card"><h3>Progress</h3><div class="value">${progress.percent}%</div><div class="sub">${progress.completed}/${progress.total} phases</div><div class="bar"><div class="fill" style="width:${progress.percent}%"></div></div></div>
</div><div class="footer">Generated by Systematize Framework v2 — ${new Date().toISOString().slice(0,16)}</div></body></html>`;

  const exportsDir = join(env.REPO_ROOT, '.Systematize/exports');
  ensureDir(exportsDir);
  const outputPath = opts.output || join(exportsDir, `${branch}-dashboard.html`);
  writeFileSync(outputPath, html, 'utf8');

  if (opts.json) {
    console.log(JSON.stringify({ branch, outputPath, stats: { frCount, nfrCount, acCount, rkCount, taskCount, completedTasks, progress: progress.percent, healthScore: health.score, healthStatus: health.status } }));
  } else {
    console.log(`✅ Dashboard exported: ${outputPath}`);
  }
}
